        let messageBox = document.getElementById("mensagemRetorno");
        messageBox.innerHTML = ""; // Limpa mensagens anteriores

        // Cores e ícones corporativos
        let bgColor = data.status === "success" ? "#1E88E5" : "#D32F2F"; // Azul para sucesso, vermelho para erro
        let icon = data.status === "success" ? "✔️" : "❌";
        
        messageBox.innerHTML = `
            <div style="
                max-width: 500px;
                margin: 20px auto;
                background: ${bgColor};
                color: white;
                padding: 20px;
                border-radius: 10px;
                box-shadow: 0 4px 8px rgba(0, 0, 0, 0.2);
                text-align: center;
                font-family: Arial, sans-serif;
            ">
                <h2 style="margin-bottom: 10px; font-size: 22px; color: white;">${icon} ${data.status === "success" ? "Mensagem Enviada!" : "Erro no Envio"}</h2>
                <p style="font-size: 16px; line-height: 1.5; color: white;">${data.message}</p>
            </div>
        `;
    })
    .catch(error => {
        console.error("Erro ao enviar requisição:", error);
    });
});
